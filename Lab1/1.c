#include <stdio.h>

int main(){
	int a, b;
	printf("Enter a number: ");
	scanf("%d", &a);
	printf("Enter another number: ");
	scanf("%d", &b);
	printf("sum: %d\n", a+b);
	printf("product: %d\n", a*b);
	printf("difference: %d\n", a-b);
	printf("quotient: %d\n", a/b);
	printf("remainder: %d\n", a%b);
	
} 
